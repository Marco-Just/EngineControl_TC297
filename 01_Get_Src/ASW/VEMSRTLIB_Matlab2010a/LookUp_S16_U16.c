void LookUp_S16_U16( SInt16 *pY, const SInt16 *pYData, UInt16 u, const
                    UInt16 *pUData, UInt32 iHi)
{
  UInt32 iLeft;
  UInt32 iRght;
  BINARYSEARCH_U16( &(iLeft), &(iRght), u, pUData, iHi);
  INTERPOLATE_S16_U16( pY, pYData[iLeft], pYData[iRght], u, pUData[iLeft],
                      pUData[iRght]);
}

